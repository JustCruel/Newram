    <script>
        // Show/Hide Day Selection
        document.getElementById('include_day').addEventListener('change', function () {
            document.getElementById('daySelection').style.display = this.checked ? 'block' : 'none';
        });

        // Handle form submission with AJAX
        document.getElementById('filterForm').addEventListener('submit', function (e) {
            e.preventDefault();
            const formData = new FormData(this);

            fetch('filter_revenue.php', {
                method: 'POST',
                body: formData,
            })
                .then(response => response.text())
                .then(data => {
                    document.getElementById('revenueDisplay').innerHTML = data;
                    updateChart(); // Update the chart based on new data
                })
                .catch(error => console.error('Error:', error));
        });

        // Update the chart
        function updateChart() {
            const ctx = document.getElementById('revenueChart').getContext('2d');
            const revenueData = <?php echo json_encode(array_values($dailyRevenue)); ?>; // Array of daily revenues
            const labels = Array.from({ length: revenueData.length }, (_, i) => i + 1); // Days of the month

            new Chart(ctx, {
                type: 'bar',
                data: {
                    labels: labels,
                    datasets: [{
                        label: 'Daily Revenue',
                        data: revenueData,
                        backgroundColor: 'rgba(75, 192, 192, 0.2)',
                        borderColor: 'rgba(75, 192, 192, 1)',
                        borderWidth: 1
                    }]
                },
                options: {
                    scales: {
                        y: {
                            beginAtZero: true
                        }
                    }
                }
            });
        }

        // Handle PDF generation
        document.getElementById('generatePdfBtn').addEventListener('click', function () {
            const form = document.getElementById('filterForm');
            form.action = '?generate_pdf=1';
            form.submit();
        });
    </script>
