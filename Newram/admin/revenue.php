                    <select name="month" id="month" class="form-select" required>
                        <?php for ($m = 1; $m <= 12; $m++) {
                            $selected = ($m == $currentMonth) ? 'selected' : '';
                            echo "<option value='$m' $selected>" . date("F", mktime(0, 0, 0, $m, 1)) . "</option>";
                        } ?>
